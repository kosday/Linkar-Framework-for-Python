"""
    enum: ASCII_Chars
    Some ASCII characters used by Linkar.
    ---Code
        US = 0x1F
        FS = 0x1C
        RS = 0x1E
    ---
"""
class ASCII_Chars:
    US = 0x1F
    US_chr = '\u001F'
    US_str = "\u001F"

    FS = 0x1C
    FS_chr = '\u001C'
    FS_str = "\u001C"

    RS = 0x1E
    RS_chr = '\u001E'
    RS_str = "\u001E"
